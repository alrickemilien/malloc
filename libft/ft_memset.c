#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	(void)c;
	i = 0;
	while (i < n)
	{
		((char*)s)[i] = (char)c;
		i++;
	}
	return ((void*)s);
}
