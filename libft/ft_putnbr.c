#include "libft.h"

void	ft_putnbr(int n)
{
	int		m;

	if (n < 0)
		ft_putchar('-');
	m = 1;
	while ((n / m) >= 10 || n / m <= -10)
		m *= 10;
	while (m >= 1)
	{
		if (n < 0)
			ft_putchar(-((n / m) % 10) + 48);
		if (n > 0)
			ft_putchar((n / m) % 10 + 48);
		if (n == 0)
			ft_putchar(48);
		m = m / 10;
	}
}
