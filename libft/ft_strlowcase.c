#include "libft.h"

void		ft_strlowcase(char *s)
{
	char	*tmp;

	tmp = s;
	while (tmp && *tmp)
	{
		if (*tmp >= 'A' && *tmp <= 'Z')
			*tmp = (*tmp + 32);
		tmp++;
	}
}
